#include "ft_ls.h"

size_t		ft_count_num_len(intmax_t n, short int base)
{
	size_t		i;

	i = (n >= 0) ? 1 : 2;
	while (n / base != 0)
	{
		n /= base;
		i++;
	}
	return (i);
}
